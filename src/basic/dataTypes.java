package basic;

public class dataTypes {
    public static void main(String[] args) {
        byte age;
        age = 127;
        System.out.println("this is byte "+age);
        System.out.println(Byte.MAX_VALUE);
        System.out.println(Byte.MIN_VALUE);

        int x = 5;
        System.out.println(x);
        System.out.println(Integer.MAX_VALUE);

        long a = 1123213131;
        System.out.println(a);
        System.out.println(Long.MAX_VALUE);

        float GPA = 3;
        System.out.println(GPA);

        double CGP = 7.5;
        System.out.println(CGP);

        boolean state = true;  // either true=1 or false=0
        System.out.println(state);

        // char grade = 'A'; // used for only 1 character
        // String m = "prashant"; // used for multiple character

        // final int i=10; // used for constant values

    }
}


